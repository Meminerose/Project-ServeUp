    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        PayrollModel model = new PayrollModel();
        model.setIdEmployee(txtID.getText());
        try {
            String startStr = txtStart.getText(); // "2025-06-01"
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            java.util.Date parsed = sdf.parse(startStr);
            java.sql.Date sqlDate = new java.sql.Date(parsed.getTime());
            model.setPeriodStart(sqlDate);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Format tanggal tidak valid (yyyy-MM-dd)");
            return;
        }

        String bonusStr = txtBonus.getText();
        double bonus = bonusStr.isEmpty() ? 0 : Double.parseDouble(bonusStr);
        if (bonus < 0) {
            JOptionPane.showMessageDialog(this, "Bonus tidak boleh bernilai negatif.");
            return;
        }
        model.setBonus((int) bonus);

        model.setPaymentMethod(cbPayment.getSelectedItem().toString());
        model.setStatus(cbStatus.getSelectedItem().toString());
        model.setNotes(txtNotes.getText());

        int month = mcFilterBulan.getMonth() + 1;
        int year = ycFilterTahun.getYear();

        controller.updatePayroll(model, tbPayroll, month, year);
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnBayarGajiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarGajiActionPerformed
        // TODO add your handling code here:
        String id = txtID.getText();
        int bulan = mcFilterBulan.getMonth() + 1; // JMonthChooser: 0-based index
        int tahun = ycFilterTahun.getYear();
        String notes = txtNotes.getText();
        
        PayrollModel model = new PayrollModel();
        model.setIdEmployee(id);
        model.setBulan(bulan);
        model.setTahun(tahun);
        model.setNotes(notes);
        model.setRecipientEmail(txtEmail.getText());
        model.setSlipFile(new File("slip/SlipGaji_" + txtID.getText() + "_" + model.getBulan() + "_" + model.getTahun() + ".pdf"));
        
        boolean success = controller.bayarGaji(model);
        
        if (success) {
            File slip = controller.generateSlipGaji(model);

            if (slip != null && slip.exists()) {
                controller.sendSlipToEmail(model);
            } else {
                JOptionPane.showMessageDialog(null, "Slip gaji gagal dibuat atau tidak ada data payroll untuk bulan ini.");
            }
        }
    }//GEN-LAST:event_btnBayarGajiActionPerformed

