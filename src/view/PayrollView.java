    private void tbPayrollMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbPayrollMouseClicked
        // TODO add your handling code here:
        int row = tbPayroll.getSelectedRow();
        if (row >= 0) {
            String idUser = tbPayroll.getValueAt(row, 0) != null ? tbPayroll.getValueAt(row, 0).toString() : "";
            String nama = tbPayroll.getValueAt(row, 1) != null ? tbPayroll.getValueAt(row, 1).toString() : "";
            String periodStart = tbPayroll.getValueAt(row, 2) != null ? tbPayroll.getValueAt(row, 2).toString() : "";
            String periodEnd = tbPayroll.getValueAt(row, 3) != null ? tbPayroll.getValueAt(row, 3).toString() : "";
            String baseSalary = tbPayroll.getValueAt(row, 4) != null ? tbPayroll.getValueAt(row, 4).toString() : "0";
            String bonus = tbPayroll.getValueAt(row, 5) != null ? tbPayroll.getValueAt(row, 5).toString() : "0";
            String paymentMethod = tbPayroll.getValueAt(row, 7) != null ? tbPayroll.getValueAt(row, 7).toString() : "";
            String status = tbPayroll.getValueAt(row, 8) != null ? tbPayroll.getValueAt(row, 8).toString() : "";
            String notes = tbPayroll.getValueAt(row, 10) != null ? tbPayroll.getValueAt(row, 10).toString() : "";

            txtID.setText(idUser);
            txtNama.setText(nama);
            txtStart.setText(periodStart);
            txtEnd.setText(periodEnd);
            txtBaseSalary.setText(baseSalary);
            txtBonus.setText(bonus);
            cbPayment.setSelectedItem(paymentMethod);
            cbStatus.setSelectedItem(status);
            txtNotes.setText(notes);

            txtID.setEnabled(false);
            txtNama.setEnabled(false);
            txtStart.setEnabled(false);
            txtEnd.setEnabled(false);
            txtBaseSalary.setEnabled(false);

            // Ambil email dari tb_user
            try {
                Connection conn = koneksi.getConnection();
                String query = "SELECT email FROM tb_user WHERE id_employee = ?";
                PreparedStatement ps = conn.prepareStatement(query);
                ps.setString(1, idUser);
                ResultSet rs = ps.executeQuery();

                if (rs.next()) {
                    txtEmail.setText(rs.getString("email"));
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error ambil email: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_tbPayrollMouseClicked

