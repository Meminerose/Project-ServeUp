    private void dcFilterTanggalPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_dcFilterTanggalPropertyChange
        // TODO add your handling code here:
        if ("date".equals(evt.getPropertyName())) {
            Date selectedDate = ((JDateChooser) evt.getSource()).getDate();
            if (selectedDate != null) {
                controller.loadKaryawanByTanggal(selectedDate, tbKaryawan);
            }
        }
    }//GEN-LAST:event_dcFilterTanggalPropertyChange

