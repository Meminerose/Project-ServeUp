    private void tbKaryawanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbKaryawanMouseClicked
        // TODO add your handling code here:
        int selectedRow = tbKaryawan.getSelectedRow();
        if (selectedRow >= 0) {
            String id_emp = tbKaryawan.getValueAt(selectedRow, 0).toString();
            String nama_emp = tbKaryawan.getValueAt(selectedRow, 1).toString();
            String start = tbKaryawan.getValueAt(selectedRow, 3).toString();
            String end = tbKaryawan.getValueAt(selectedRow, 4).toString();

            Object statusObj = tbKaryawan.getValueAt(selectedRow, 7);
            String status = statusObj != null ? statusObj.toString() : null;

            txtID.setText(id_emp);
            txtNama.setText(nama_emp);
            txtShiftStart.setText(start);
            txtShiftEnd.setText(end);

            if (status != null) {
                cbbKeterangan.setSelectedItem(status);
            } else {
                cbbKeterangan.setSelectedIndex(1); // atau 0, sesuai default-mu
            }

            btnCheckIn.setEnabled(true);
            btnCheckOut.setEnabled(true);
            txtID.setEnabled(false);
            txtNama.setEnabled(false);
            txtShiftStart.setEnabled(false);
            txtShiftEnd.setEnabled(false);
        }
    }//GEN-LAST:event_tbKaryawanMouseClicked

